              Cada estilo de cabelo tem o tempo em que devemos lavá-los e qual temperatura que é ideal. Como por exemplo:
           </p>
           <p>
              <strong>Oleosos/mistos:</strong> Água morna ou fria, uma vez que a temperatura quente aumenta a oleosidade do cabelo. E lavar um dia sim, um dia não.
           </p>
           <p>
              <strong>Cabelos normais:</strong> Já que esse tipo de cabelo não sofre nenhuma alteração como por exemplo a oleosidade, o recomendado lavar a cada dois dias.
           </p>
           <p>
              <strong>Cabelos ressecados/secos:</strong> Como a raiz demora pra ficar oleosa, o mais recomendado é lavar de 2 a 3 vezes por semana, já é o suficiente.
