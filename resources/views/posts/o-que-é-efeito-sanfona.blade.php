                  Aposto que você já ouviu a seguinte frase: “Emagreci 2kg semana passada, mas, depois desse final de semana devo ter engordado o dobro”, mas, saiba que esse é o principal motivo do efeito sanfona ou conhecido também como fenômeno ioiô. 
                  Quando começamos uma dieta ou algum exercício físico, automaticamente, nosso pensamento se volta a perder peso (óbvio), ficamos empolgados e até conseguimos chegar no resultado esperado ou pelo menos, perto. Porém, muitos relatam que o mais difícil desse processo todo, é manter o peso e a dieta após isso.
                  Então, vamos entender um pouco desse mal que cerca a maioria do pessoal que entra nos hábitos de alimentação saudável e exercícios físicos.
               </p>
               <p>
                  Para começarmos, vamos começar primeiramente entendendo um pouquinho do efeito sanfona. O que nada mais é uma perda brusca de peso, consequência de uma restrição alimentar e seu desempenho na academia, seguida de uma recuperação do peso perdido. O qual pode virar um ciclo na vida de muitos dos atletas (e acredite, isso é mais comum que você pensa).
               </p>
               <p>
                  Cabe ressaltar que isso muitas vezes ou diria que a maioria dos casos isso acontece pelo fato de começarem uma dieta sem acompanhamento de um profissional da área, e ficam fazendo aquelas “dietas milagrosas” da moda. Não que seja errado quem está com sobrepeso buscar por um corpo mais saudável, porém a busca sem a devida orientação e acompanhamento especializado pode colocar tudo a perder. Às vezes o jeito mais fácil e rápido pode lhe presentear com o sobrepeso depois. Porque não é nada fácil fazer dieta e conseguir chegar no objetivo, agora, imagina sem um acompanhamento de uma pessoa que entende esse universo.                            
               </p>
               <p>
                  Uma possível explicação para o efeito sanfona é que o organismo tem uma espécie de memória metabólica, ou seja, o funcionamento do corpo deseja ficar como está, sem mudanças drásticas.
                  E se já não bastasse, o efeito sanfona pode causa um efeito negativo para sua saúde, como hipertensão, dislipidemias e diabetes. Com as falhas que costumamos ter por isso, acabamos desistindo da meta que traçamos e voltamos a ter maus hábitos, até nos piores casos, voltando a comer alimentos gordurosos e ricos em açúcar em excesso, que aumentam as chances das doenças crônicas.
