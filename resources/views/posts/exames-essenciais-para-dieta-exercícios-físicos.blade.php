                <p>
                  Como poucos sabem, o item principal e me arriscarei de dizer que o mais importante antes de começar uma dieta e exercícios físicos são os exames diagnósticos. E sim, você está enganado se pensa que esses tipos de procedimentos são feitos somente para descobrir doenças e problemas no seu corpo. Eles são essenciais para conhecer, verificar e confirmar a sua patologia e prevenir possíveis complicações que você possa ter com seus treinos e vida que está preste a começar a ter.
                </p>
                <p>
                  Muitas antes de tomar a decisão de começar a ter uma vida saudável, gostavam de visitar seus fast food preferidos e não praticavam nem ao menos uma pedalada até seu trabalho e isso pode ou não complicar para seu organismo. São eles que permitem perceber se seu corpo está funcionando de forma plena e correta.
                  Para quem quer começar a mudar seus hábitos, como por exemplo, praticar algum esporte ou fazer academia, há a necessidade de conhecer seu corpo antes e depois de sair do sedentarismo.
                </p>
                <h4>Os principais exames listados são:</h4>
                <p><strong>Exame de glicemia:</strong> Seu objetivo é verificar o nível de açúcar do sangue, para informar se a pessoa é diabética ou não, ou se tem alguma deficiência de insulina.
                  Com o resultado do exame, o seu médico terá que dar uma atenção especial a sua dieta, ou para controlar ou prevenir a doença.</p>
              
                <p><strong>Exame de sangue:</strong> Esse exame podemos verificar alguns pontos importantes para ajudar na sua dieta, uma vez que alterado pode prejudicar até mesmo na sua perca de peso, por exemplo: taxas de colesterol, triglicérides, índices de hormônio e hormônio da tireoide. Todos sendo possíveis detectar com uma simples coleta de sangue.</p>
            
                <p><strong>Exames cardíacos:</strong> Quando você pensa em começar a suas atividades físicas, logo já pensamos em potencializar o seu dia-dia para melhores e agilizar os resultados. Nesse caso são recomendados os exames de eletrocardiograma, eco cardiograma e o teste ergométrico. Não pode querer forçar o seu corpo e seu coração com exercícios, sem antes verificar a sua resistência física e seus limites.</p>
            
                <p><strong>Colesterol:</strong> Um ponto muito importante de manter os níveis de colesterol normais é a saúde das células e para produção de hormônios do corpo. Assim, também evitando o risco de doenças coronárias.</p>
           
                <p><strong>Emograma:</strong> E por fim, porém, não menos importante este exame é essencial para detectar possível existência de anemia, processos infecciosos bacterianos ou virais.</p>                            
                <p>
                  Depois de todas essas dicas de exames que é super interessante de fazer antes de começar a sua dieta, lembramos-lhe que a maior e mais importante dica é: procure um médico da área para lhe orientar e solicitar todos os procedimentos necessários para o seu corpo, sua necessidade e para você começar de maneia segura e com melhores resultados.
                  Sua saúde sempre e exclusivamente virá em primeiro lugar, então, seus esforços podem alcançar resultados mais rápidos e eficazes, já que é possível começar sua dieta com alimentação e exercícios elaborados para sua necessidade, biótipo e características.
                </p>
